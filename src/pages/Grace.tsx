import { useState, useRef, useEffect } from 'react'
import { motion } from 'framer-motion'
import { Send, MessageCircle } from 'lucide-react'
import { sendMessage, type Message } from '../lib/api'

const quickActions = [
  'I have storm damage on my roof',
  'How does the insurance claims process work?',
  'What will I have to pay out of pocket?',
  'Schedule a free inspection',
  'What areas do you serve?',
  'Tell me about Guardian',
]

export default function Grace() {
  const [messages, setMessages] = useState<Message[]>([
    {
      role: 'assistant',
      content:
        "Hi there! I'm Grace, Guardian Roofing & Siding's AI claims specialist. I'm here to help you understand the storm damage insurance claims process, schedule inspections, or answer any questions about our services. How can I help you today?",
    },
  ])
  const [input, setInput] = useState('')
  const [isLoading, setIsLoading] = useState(false)
  const messagesEndRef = useRef<HTMLDivElement>(null)
  const inputRef = useRef<HTMLInputElement>(null)

  useEffect(() => {
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' })
  }, [messages])

  useEffect(() => {
    inputRef.current?.focus()
  }, [])

  const handleSend = async (text?: string) => {
    const userMessage = text || input.trim()
    if (!userMessage || isLoading) return

    setInput('')
    const newMessages: Message[] = [...messages, { role: 'user', content: userMessage }]
    setMessages(newMessages)
    setIsLoading(true)

    const data = await sendMessage(newMessages)
    setMessages((prev) => [...prev, { role: 'assistant', content: data.response }])
    setIsLoading(false)
  }

  const handleKeyDown = (e: React.KeyboardEvent) => {
    if (e.key === 'Enter' && !e.shiftKey) {
      e.preventDefault()
      handleSend()
    }
  }

  return (
    <section className="py-10 lg:py-16">
      <div className="max-w-3xl mx-auto px-4 sm:px-6">
        {/* Header */}
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.6 }}
          className="text-center mb-8"
        >
          <img
            src="/images/avatar-grace-large.webp"
            alt="Grace AI Assistant"
            className="w-20 h-20 rounded-full mx-auto mb-4 object-cover shadow-lg"
          />
          <h1 className="text-3xl sm:text-4xl font-extrabold text-navy mb-2">
            Ask <span className="text-guardian-blue">Grace</span>
          </h1>
          <p className="text-text-secondary">
            Your AI-powered insurance claims specialist. Ask anything about storm damage, claims, or our services.
          </p>
        </motion.div>

        {/* Chat Area */}
        <div className="bg-white rounded-2xl border border-border shadow-sm overflow-hidden">
          {/* Messages */}
          <div className="h-[400px] overflow-y-auto p-6 space-y-4">
            {messages.map((msg, i) => (
              <div
                key={i}
                className={`flex ${msg.role === 'user' ? 'justify-end' : 'justify-start'}`}
              >
                {msg.role === 'assistant' && (
                  <img
                    src="/images/avatar-grace.webp"
                    alt="Grace"
                    className="w-8 h-8 rounded-full mr-3 shrink-0 mt-1"
                  />
                )}
                <div
                  className={`max-w-[75%] px-4 py-3 rounded-2xl text-sm leading-relaxed ${
                    msg.role === 'user'
                      ? 'bg-guardian-blue text-white rounded-br-md'
                      : 'bg-surface text-navy border border-border rounded-bl-md'
                  }`}
                >
                  {msg.content}
                </div>
              </div>
            ))}

            {isLoading && (
              <div className="flex justify-start">
                <img
                  src="/images/avatar-grace.webp"
                  alt="Grace"
                  className="w-8 h-8 rounded-full mr-3 shrink-0"
                />
                <div className="bg-surface border border-border rounded-2xl rounded-bl-md px-4 py-3">
                  <div className="flex gap-1.5">
                    <span className="w-2 h-2 bg-navy/30 rounded-full animate-bounce [animation-delay:0ms]" />
                    <span className="w-2 h-2 bg-navy/30 rounded-full animate-bounce [animation-delay:150ms]" />
                    <span className="w-2 h-2 bg-navy/30 rounded-full animate-bounce [animation-delay:300ms]" />
                  </div>
                </div>
              </div>
            )}

            {/* Quick Actions */}
            {messages.length === 1 && !isLoading && (
              <div className="flex flex-wrap gap-2 pt-2">
                {quickActions.map((action) => (
                  <button
                    key={action}
                    onClick={() => handleSend(action)}
                    className="px-3 py-1.5 bg-guardian-blue/10 text-guardian-blue text-xs font-medium rounded-full hover:bg-guardian-blue/20 transition-colors"
                  >
                    {action}
                  </button>
                ))}
              </div>
            )}

            <div ref={messagesEndRef} />
          </div>

          {/* Input */}
          <div className="px-6 py-4 border-t border-border bg-surface/50">
            <div className="flex items-center gap-3">
              <input
                ref={inputRef}
                type="text"
                value={input}
                onChange={(e) => setInput(e.target.value)}
                onKeyDown={handleKeyDown}
                placeholder="Ask about storm damage, insurance claims, inspections..."
                disabled={isLoading}
                className="flex-1 px-4 py-3 bg-white border border-border rounded-full text-sm text-navy placeholder:text-navy/30 focus:outline-none focus:ring-2 focus:ring-guardian-blue/30 focus:border-guardian-blue disabled:opacity-50 transition-all"
              />
              <button
                onClick={() => handleSend()}
                disabled={!input.trim() || isLoading}
                className="p-3 bg-guardian-blue text-white rounded-full hover:bg-sky-600 disabled:opacity-40 transition-colors"
                aria-label="Send message"
              >
                {isLoading ? (
                  <MessageCircle className="w-5 h-5 animate-pulse" />
                ) : (
                  <Send className="w-5 h-5" />
                )}
              </button>
            </div>
          </div>
        </div>
      </div>
    </section>
  )
}
